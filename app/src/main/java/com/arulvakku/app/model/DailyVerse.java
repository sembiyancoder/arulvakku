package com.arulvakku.app.model;

public class DailyVerse {

    private String mVerseInfo;
    private String nVerseFullNo;
    private String mVerse;
    private String mDate;

    public String getmVerseInfo() {
        return mVerseInfo;
    }

    public String getnVerseFullNo() {
        return nVerseFullNo;
    }

    public void setnVerseFullNo(String nVerseFullNo) {
        this.nVerseFullNo = nVerseFullNo;
    }

    public void setmVerseInfo(String mVerseInfo) {
        this.mVerseInfo = mVerseInfo;
    }

    public String getmVerse() {
        return mVerse;
    }

    public void setmVerse(String mVerse) {
        this.mVerse = mVerse;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }
}
